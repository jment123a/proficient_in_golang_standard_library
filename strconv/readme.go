/*
文件命名规则
	atob：ascii to boolean，字符串转布尔类型
	atof：ascii to float，字符串转float64类型
	ftoa：float to ascii，float64转字符串

函数命名规则
	Parsexxx：将字符串转为xxx类型
	Formatxxx：将xxx类型转为字符串
	Appendxxx：将xxx类型转为字符串追加至数据对象中

名词
	科学计数法：mantissa x Base^Exponent
		例：1e2，mantissa = 1，base = 10，exp = 2
		mantissa：尾数
		base：基数（进制）
		exponent（exp）：指数


*/